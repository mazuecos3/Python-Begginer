# Calculamos el area de el rectangulo
def area_rectangulo():
    base = 5
    altura = 9
    resultado = base * altura
    return resultado


# Calculamos el area de el Triangulo
def area_triangulo():
    base = 5
    altura = 13
    resultado = base * altura / 2
    return resultado 


# Calculamos el area de el circulo
def area_circulo():
    pi = 3.14
    radio = 5
    resultado = pi * (radio**2)
    return resultado